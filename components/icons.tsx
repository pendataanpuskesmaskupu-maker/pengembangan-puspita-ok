// Array of TailwindCSS background color classes for the user avatar
const avatarColors = [
  'bg-red-500', 'bg-orange-500', 'bg-amber-500', 'bg-yellow-500',
  'bg-lime-500', 'bg-green-500', 'bg-emerald-500', 'bg-teal-500',
  'bg-cyan-500', 'bg-sky-500', 'bg-blue-500', 'bg-indigo-500',
  'bg-violet-500', 'bg-purple-500', 'bg-fuchsia-500', 'bg-pink-500', 'bg-rose-500'
];

// Simple hash function to get a consistent color for a given username
const stringToColorIndex = (str: string): number => {
    if (!str) return 0;
    let hash = 0;
    for (let i = 0; i < str.length; i++) {
        hash = str.charCodeAt(i) + ((hash << 5) - hash);
        hash = hash & hash; // Ensure 32bit integer
    }
    return Math.abs(hash) % avatarColors.length;
};

export const UserProfileIcon: React.FC<{ username: string | null; profilePic?: string | null }> = ({ username, profilePic }) => {
    if (profilePic) {
        return (
            <img 
                src={profilePic} 
                alt={username || 'User Profile'} 
                className="w-full h-full object-cover" 
            />
        );
    }

    if (!username) {
        return <UserCircleIcon />; // Fallback to the original icon if no username
    }

    const initial = username.charAt(0).toUpperCase();
    const colorClass = avatarColors[stringToColorIndex(username)];

    return (
        <div className={`w-full h-full rounded-full flex items-center justify-center ${colorClass}`}>
            <span className="text-white font-bold text-lg select-none">{initial}</span>
        </div>
    );
};

